-- 5. Criação da Tabela de Cores do Dia
CREATE TABLE IF NOT EXISTS chronos_day_colors (
  date_key TEXT PRIMARY KEY,
  cat_id NUMERIC
);

-- 6. Criação da Tabela de Configurações Genéricas
CREATE TABLE IF NOT EXISTS chronos_settings (
  key TEXT PRIMARY KEY,
  value TEXT
);

-- 7. Criação do Histórico do Oráculo
CREATE TABLE IF NOT EXISTS chronos_chat_history (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  role TEXT,
  text TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

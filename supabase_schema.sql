-- 1. Criação da Tabela de Tarefas
CREATE TABLE IF NOT EXISTS chronos_tasks (
  id NUMERIC PRIMARY KEY, 
  title TEXT NOT NULL,
  time TEXT,
  type TEXT,
  done BOOLEAN DEFAULT FALSE,
  note TEXT,
  date TEXT,
  end_date TEXT,
  alarm_time TEXT,
  alarm_enabled BOOLEAN DEFAULT FALSE,
  recurrence TEXT DEFAULT 'none'
);

-- 2. Criação da Tabela de Eventos Personalizados
CREATE TABLE IF NOT EXISTS chronos_custom_events (
  id TEXT PRIMARY KEY,
  name TEXT NOT NULL,
  date TEXT,
  end_date TEXT,
  logo TEXT,
  emoji TEXT,
  category TEXT,
  border_color TEXT,
  recurrence TEXT,
  description TEXT,
  details JSONB
);

-- 3. Criação da Tabela de Substituição de Eventos (Overrides)
CREATE TABLE IF NOT EXISTS chronos_event_overrides (
  id TEXT PRIMARY KEY,
  name TEXT,
  border_color TEXT,
  custom_logo TEXT
);

-- 4. Criação da Tabela de Categorias de Cores
CREATE TABLE IF NOT EXISTS chronos_color_categories (
  id NUMERIC PRIMARY KEY,
  name TEXT,
  color TEXT
);

